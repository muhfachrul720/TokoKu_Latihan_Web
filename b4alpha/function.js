function SellerSignPop(){
    document.getElementById('SignIn').style.opacity="1";
    document.getElementById('SignIn').style.visibility="visible";
    document.getElementById('SignIn').style.backgroundColor="rgba(0,0,0,.5)";
}
function SellerSignOut(){
    document.getElementById('SignIn').style.opacity="0";
    document.getElementById('SignIn').style.visibility="hidden";
}

